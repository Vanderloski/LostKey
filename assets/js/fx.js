function DRIVEINTHEATER() {
    $('body').addClass('body-drive-in-fx');
    $('#wrapper').addClass('wrapper-drive-in-fx');
    $('#banner').addClass('banner-drive-in-fx');
}

function DRIVEINTHEATER_STOP() {
    $('body').removeClass('body-drive-in-fx');
    $('#wrapper').removeClass('wrapper-drive-in-fx');
    $('#banner').removeClass('banner-drive-in-fx');
}